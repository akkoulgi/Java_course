package loops;

public class ForInfiniteLoop {

	public static void main(String[] args) {

		for(int i=1;i>=0;i++) {
			
			System.out.println(i);
			
		}
		
		//logic here is To run infinitely , it should never exit the loop.
		
		//TO never exit the loop - the condition should be always true.
	}

}
