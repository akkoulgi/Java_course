package loops;

public class forDemo {

	public static void main(String[] args) {

		
		// Print numbers from 1 to 50 using for loop
		
		for(int i =1;i<=50;i++) { // defining a loop starting from 1 to 50
			
			if(i%2==0) { // checking whether i when divided by , the remainder should be 0
			
			System.out.println(i);
			}
		}
		
		
	// for i =51, the condition fails, hence becomes false, and 
		//the control comes out of the loop. 
	}

}
